import numpy as np


arr1 = np.array([1,2,3,4,5])


arr2 = np.array([[1,2,3],
                 [4,5,6]])

print("1D Array:", arr1)
print("Datatype:", arr1.dtype)
print("Shape:", arr1.shape)
print("Dimension:", arr1.ndim)

print("\n2D Array:", arr2)
print("Datatype:", arr2.dtype)
print("Shape:", arr2.shape)
print("Dimension:", arr2.ndim)
